def super_fizzbuzz(array)
  array.map do |i|
    if i % 3 == 0 && i % 5 != 0
      i = "Fizz"
    elsif i % 5 == 0 && i % 3 != 0
      i = "Buzz"
    elsif i % 3 == 0 && i % 5 == 0
      i = "FizzBuzz"
    else
      i
    end
  end
end



